using System;

namespace MedaillesOpdracht
{
    internal class opdracht4
    {
        int geboorteMaand = 0;

        public void Start()
        {
            Console.WriteLine("In welk jaar ben je geboren?");
            int geboorteJaar = Convert.ToInt32(Console.ReadLine());

            Console.WriteLine("In welke maand ben je geboren? (bijv. Januari)");
            string geboorteMaandNaam = Console.ReadLine();

            Console.WriteLine("Op welke dag ben je geboren?");
            int geboorteDag = Convert.ToInt32(Console.ReadLine());

            if (geboorteMaandNaam == "Januari") geboorteMaand = 1;
            else if (geboorteMaandNaam == "Februari") geboorteMaand = 2;
            else if (geboorteMaandNaam == "Maart") geboorteMaand = 3;
            else if (geboorteMaandNaam == "April") geboorteMaand = 4;
            else if (geboorteMaandNaam == "Mei") geboorteMaand = 5;
            else if (geboorteMaandNaam == "Juni") geboorteMaand = 6;
            else if (geboorteMaandNaam == "Juli") geboorteMaand = 7;
            else if (geboorteMaandNaam == "Augustus") geboorteMaand = 8;
            else if (geboorteMaandNaam == "September") geboorteMaand = 9;
            else if (geboorteMaandNaam == "Oktober") geboorteMaand = 10;
            else if (geboorteMaandNaam == "November") geboorteMaand = 11;
            else if (geboorteMaandNaam == "December") geboorteMaand = 12;
            else
            {
                Console.WriteLine("Onbekende maand ingevoerd.");
                return;
            }

            DateTime geboortedatum = new DateTime(geboorteJaar, geboorteMaand, geboorteDag);

            DateTime vandaag = DateTime.Today;

            int leeftijd = vandaag.Year - geboortedatum.Year;
            if (vandaag < geboortedatum.AddYears(leeftijd))
            {
                leeftijd--;
            }






















































































































































































































































































































































































































































































































            DateTime geboortedatumHugo = new DateTime(2008, 3, 15);

            if (geboortedatumHugo.ToString("dd-MM-yyyy") == "15-03-2008")
            {
                Console.WriteLine("hoi hugo");
            }



            Console.WriteLine($"Je bent geboren op {geboortedatum:dd-MM-yyyy}.");
            Console.WriteLine($"Je bent {leeftijd} jaar oud.");
        }
    }
}